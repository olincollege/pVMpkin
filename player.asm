    .ORIG x3000

    LD R0, AUDIO_START
    LD R1, AUDIO_END

LOOP
    LDR R2, R0, #0
    STI R2, MR_AUDIO_DATA

    ADD R0, R0, #1

    NOT R4, R0
    ADD R4, R4, #1
    ADD R3, R1, R4

    BRzp CONTINUE

    LD R0, AUDIO_START
    BR LOOP

CONTINUE
    JSR DELAY
    BR LOOP

DELAY
    LD R4, DELAY_COUNT
DELAY_LOOP
    ADD R4, R4, #-1
    BRp DELAY_LOOP
    RET

AUDIO_START .FILL x5000
AUDIO_END .FILL xEC40
MR_AUDIO_DATA .FILL xFE04
DELAY_COUNT .FILL #300

.END
